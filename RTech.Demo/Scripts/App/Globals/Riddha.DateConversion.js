    function ConvertBsToAd(yyyy, mm, dd) {
        var en_startDate = arrNepaliDates[yyyy][0].split('/');
        var en_yyyy = en_startDate[2];
        var en_mm = en_startDate[1];
        var en_dd = en_startDate[0];
        var daysCount = dd - 1;
        for (var i = 1; i < mm; i++) {
            daysCount += arrNepaliDates[yyyy][i];
        }
        var date = en_yyyy + '/' + en_mm + '/' + en_dd + '/';
        var date = new Date(date);
        new Date(date.setDate(date.getDate() + parseInt(daysCount)));
        var day = date.getDate().toString();
        day = day.length == 1 ? '0' + day : day;
        var month = (date.getMonth() + 1).toString();
        month = month.length == 1 ? '0' + month : month;
        var ad = date.getFullYear() + '/' + month + '/' + day;
        return ad;
    }
    //convert ad date to bs 
    function ConvertAdToBs(date, callback) {
        var adDates = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];//days for en months in non leap year
        var leapAdDates = [31, 29, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];//days for en months in leap year
        var arrDates = date.split('/');
        if (arrDates.length != 3) {
            callback(-1);
            return;
        }
        var yyyy = parseInt(arrDates[0]);
        var mm = parseInt(arrDates[1]);
        var dd = parseInt(arrDates[2]);
        var isLeapYear = yyyy % 4 == 0 ? true : false;
        var validDays;
        if (mm > 0 && mm < 13) {
            validDays = isLeapYear ? leapAdDates[mm - 1] : adDates[mm - 1];
            var bsYear = yyyy + 57;
            //get the start english date for the date for nepali year 
            var arrSoy = arrNepaliDates[bsYear][0].split('/');
            var soy = arrSoy[2] + '/' + arrSoy[1] + '/' + arrSoy[0];
            if (mm < arrSoy[1]) {
                arrNepaliDates[bsYear - 1][0].split('/');
                soy = arrSoy[2] + '/' + arrSoy[1] + '/' + arrSoy[0];
                bsYear = bsYear - 1;

            } else if (mm == arrSoy[1]) {
                if (dd < arrSoy[0]) {
                    arrNepaliDates[bsYear - 1][0].split('/');
                    soy = arrSoy[2] + '/' + arrSoy[1] + '/' + arrSoy[0];
                    bsYear = bsYear - 1;
                }

            }
            if (typeof soy == 'undefined') {
                callback(-1);
                return;
            }

            if (dd > 0 && dd <= validDays) {
                var tillDays = (Math.floor(Date.parse(soy) - Date.parse(date)) / 86400000);//no of days from start of the nepali year
                if (tillDays > 0) {
                    tillDays = isLeapYear ? 366 - tillDays : 365 - tillDays;
                }
                else { tillDays = Math.abs(tillDays); }
                var days = 0;
                var month = 01;//bs month
                var day = 01;//day of the month
                var arrNepaliMonths = arrNepaliDates[bsYear];
                $.each(arrNepaliMonths, function (index, item) {
                    days += arrNepaliDates[yyyy][index + 1];
                    if (days >= tillDays) {
                        if (days == tillDays) {
                            day = arrNepaliDates[yyyy][index + 1];//get the day of the month 
                        } else {
                            if (tillDays != 0) {
                                day = tillDays + arrNepaliDates[yyyy][index + 1] - days + 1;
                            }
                        }

                        month = index + 1;

                        return false;
                    }
                });
                month = month.toString().length == 1 ? '0' + month : month;
                day = day.toString().length == 1 ? '0' + day : day;
                var bs = bsYear + '/' + month + '/' + day;
                callback(bs);
                return;
            }
            else {
                callback(-1);
                return;
            }
        } else {
            callback(-1);
            return;
        }

        //     Math.floor((Date.parse(soy) - Date.parse(date)) / 86400000);
    }

